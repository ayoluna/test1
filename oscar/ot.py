def suma(a, b):
    resultado = a + b
    return resultado

def resta(a, b):
    resultado = a - b
    return resultado

# Ejemplos de uso
numero1 = 11
numero2 = 5

resultado_suma = suma(numero1, numero2)
resultado_resta = resta(numero1, numero2)

print(f"La suma de {numero1} y {numero2} es: {resultado_suma}")
print(f"La resta de {numero1} y {numero2} es: {resultado_resta}")